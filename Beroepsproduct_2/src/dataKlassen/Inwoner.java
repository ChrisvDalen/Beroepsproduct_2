package dataKlassen;

public class Inwoner {
	
	private String naam;
	private int leeftijd;
	
	public Inwoner() {
		this.naam = naam;
		this.leeftijd = leeftijd;
		
	}
	
	public void setNaam( ) {
		this.naam = naam;
	}
	
	public String getNaam() {
		return naam;
		
	}
	
	public void setLeeftijd() {
		this.leeftijd = leeftijd;
	}
	
	public int getLeeftijd() {
		return leeftijd;
	}
	
	

}
